package day11_set_get;

public class Quiz02_CoffeeMachine {

	private int menuNum, total;
	private String menu, hotIce, coffeeBean;
	public int getMenuNum() {
		return menuNum;
	}
	public void setMenuNum(int menuNum) {
		this.menuNum = menuNum;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public String getMenu() {
		return menu;
	}
	public void setMenu(String menu) {
		this.menu = menu;
	}
	public String getHotIce() {
		return hotIce;
	}
	public void setHotIce(String hotIce) {
		this.hotIce = hotIce;
	}
	public String getCoffeeBean() {
		return coffeeBean;
	}
	public void setCoffeeBean(String coffeeBean) {
		this.coffeeBean = coffeeBean;
	}

	
}
